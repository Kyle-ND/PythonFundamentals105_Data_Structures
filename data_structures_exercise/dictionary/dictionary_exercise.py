# Dictionary Exercises

# For each exercise, follow the TDD approach by writing test functions first 
# in the test file in this directory, making them fail, defining the function
# to make them compile, and then implementing the correct code to make the tests pass.

# Exercise 1: Managing a Music Library

# Create a music library dictionary with songs as key-value pairs.
# Add a new song to the library.
# Check if a specific song is in the library.
# Update the artist of an existing song.
# Print the names of all the songs in the library.

# Exercise 2: Working with Student Records

# Create a student records dictionary with names as keys and exam scores as values.
# Calculate and print the average score of all the students.
# Find the student with the highest score.
# Remove a student from the records.
# Add a new student and their score to the records.